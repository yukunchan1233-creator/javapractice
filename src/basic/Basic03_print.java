package basic;

public class Basic03_print {
//여기에 적는것은 멤버변수()
	public static void main(String[] args) {
		//변수란 : 처리하고자 하는 데이터를 저장하는 기억장소이다
		//메소드() 안에 있는 변수를 매개변수라고 부른다
		//String[] args : 메서드() 안에 있는 매개변수라고 부른다
		//string~~ = 매개변수
		//출력하는 방법
		System.out.print("love");
		//문자열을 출력하고 싶을 때는 " " 쌍따옴표 묶어준다
	    System.out.print("java");
	    System.out.println("java love");
		//print()  옆으로 출력 println() 출력후!!!!!줄변경 so 3가지 문자열이 다 가로로 배치되는 것
	    System.out.println('A');
	    //문자는 '' 홑따옴표로 묶는다
	    System.out.println(10);
	    System.out.println(10+20); //더한값
	    System.out.println(10/2);  //나눈값
	    System.out.println(10*20); //곱한값
	    System.out.println(10%2);  //나머지
	    System.out.println(10-5);  //뺀값
	    System.out.println("*");
	    System.out.println("**");
	    System.out.println("***");
	    System.out.println("****");
	    System.out.println("*****");
	}
}
